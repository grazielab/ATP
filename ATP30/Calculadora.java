public class Calculadora {

    public static int soma(int v1, int v2){
        return v1 + v2;
    }

    public static int sub(int v1, int v2){
        return v1 - v2;
    }

    public static int mult(int v1, int v2){
        return v1 * v2;
    }

    public static int div(int v1, int v2){
        return v1 / v2;
    }
}